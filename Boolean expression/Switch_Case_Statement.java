public class Switch_Case_Statement {
    
    public static void main(String[] args) {
        String name = "Tuesday";
        switch(name){
            case "Saturday" :
            System.out.println("To Day is Saturday.");
            break;

            case "Sunday" :
            System.out.println("To Day is Sunday.");
            break;

            case "Monday" :
            System.out.println("To Day is Monday.");
            break;

            case "Tuesday" :
            System.out.println("To Day is Tuesday.");
            break;

            case "Wednesday" :
            System.out.println("To Day is Wednesday.");
            break;

            case "Thursday" :
            System.out.println("To Day is Thursday.");
            break;

            case "Friday" :
            System.out.println("To Day is Friday.");
            break;

            default :
            System.out.println("To Day is Unknown.");

        }
        System.out.println("|");
        System.out.println("|");
        System.out.println("|");
        System.out.println("!!Exiting Switch_Case_Statement...");

    }


}
