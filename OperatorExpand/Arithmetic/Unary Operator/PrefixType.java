public class PrefixType{

    public static void main(String[] args) {
        
        int a = 50;
        int b = 80;

        // ++a standing for increase +1 with a value.
        System.out.println("The num of ++a is: " + ++a);

        // --b standing for decrease -1 with a value.
        System.out.println("The num of --b is: " + --b);

    }


}