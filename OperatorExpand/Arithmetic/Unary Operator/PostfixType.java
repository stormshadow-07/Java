public class PostfixType{

    public static void main(String[] args) {
        
        int a = 50;
        int b = 90;

        // a++ standing for increase +1 with a value in 2nd print line.
        System.out.println("Main value:"+ a++);
        System.out.println("The num of ++a is: " + a);

        // b-- standing for decrease -1 with a value in 2nd print line.
        System.out.println("Main value:" + b--);
        System.out.println("The num of b-- is: "+ b);

    }


}