public class BinaryType{

    public static void main(String[] args) {
        int a = 40;
        int b = 50;
        int c = 70;
        float f = 45.8f;
        int x = a + b;      
	    int y = c - a;      
        float z = a * f;      
        int d = b / a;    
        float m = c % a;  

        //Addition
        System.out.println("The Addition of two numbers:" + x);
        //Subtraction
        System.out.println("The Subtraction of two numbers:" + y);
        //Multiplication
        System.out.println("The Multiplication of two numbers:" + z);
        //Division 
        System.out.println("The Division of two numbers:" + d);
        //Modulus
        System.out.println("The Modulus of two numbers:" + m);
    }




}